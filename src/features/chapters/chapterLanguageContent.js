export const getLocaleRegion = () => {
  const locale = navigator?.language || navigator?.languages?.[0] || '';
  const region = locale.split('-')?.[1]?.toUpperCase();
  return /^[A-Z]{2}$/.test(region || '') ? region : null;
};

