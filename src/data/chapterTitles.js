export const getChapterDisplayTitle = (chapter, uiLanguage = 'en') => {
  if (!chapter) return '';

  if (uiLanguage !== 'ja') {
    return typeof chapter.title === 'string'
      ? chapter.title
      : (typeof chapter.title?.title === 'string' ? chapter.title.title : '');
  }

  const chapterNumber = Number(chapter.number);
  if (Number.isInteger(chapterNumber) && JAPANESE_CHAPTER_TITLES[chapterNumber]) {
    return JAPANESE_CHAPTER_TITLES[chapterNumber];
  }

  if (chapterNumber === 56.5) return '番外編';
  if (chapterNumber === 11.5 || chapterNumber === 23.5 || chapterNumber === 35.5 || chapterNumber === 41.5 || chapterNumber === 47.5 || chapterNumber === 53.1 || chapterNumber === 53.2 || chapterNumber === 53.5) {
    return '特典編';
  }
  if (chapterNumber === 81) return '2026年6月発売予定';

  if (typeof chapter.title === 'string' && chapter.title.trim()) {
    return chapter.title;
  }

  return '';
};

